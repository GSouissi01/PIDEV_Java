package Entities;

/**
 *
 * @author Safe
 */
public class Categorie {
    String name;
    int id;

    public Categorie() {
    }

    public Categorie(int id,String name) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "Categorie{" + "name=" + name + ", id=" + id + '}';
    }
    
}
